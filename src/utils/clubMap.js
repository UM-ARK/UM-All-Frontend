export function clubTagMap(tag) {
    switch (tag) {
        case 'sa':
            return '學生會';
            break;
        case 'club':
            return '學會';
            break;
        case 'society':
            return '社團';
            break;
        case 'college':
            return '書院';
            break;
        case 'official':
            return '澳大官方';
            break;
        case 'media':
            return '媒體';
            break;
        case 'business':
            return '商業';
            break;
        default:
            break;
    }
}
